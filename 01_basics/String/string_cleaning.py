# Clean Whitespaces

text = " Engineering "
print(text)

# remove leading and trailing whitespaces
print(text.strip())

# remore leading whitespaces
print(text.lstrip())

# remove trailing whitespaces.
print(text.rstrip())

# remove special characters

print("###AAAA###".strip("#"))

print("-" * 100)
