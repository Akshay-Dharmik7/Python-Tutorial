#Types
#1- type()
#2- int()
#3- float()
#4- complex()

a = 13
b = 3.14
c = 2+4j
str = "24"

print("----- type() -----")
print(type(a))
print(type(b))
print(type(c))

print("-------- int()-------")
print(int(b))
print(int(str))

print("------- float()------")
print(float(a))
print(float(str))

print("-----complex()-------")
print(complex(a,b))
