# Single list
list = [1, 3, 6, 4, 7]

# sort in accending order
new_list = sorted(list)
print("Original List: ", list)
print("Sorted List: ", new_list)


# matrix list
matrix_list = [
    ['d', 'e', 'f'],
    ['a', 'b', 'c'],
    ['g', 'h', 'i']
]

new_matrix_list = sorted(matrix_list)
print("Original List: ", matrix_list)
print("Sorted List: ", new_matrix_list)






