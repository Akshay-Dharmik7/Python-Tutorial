# Single list
list = [1, 3, 6, 4, 7]

# sort in accending order
list.sort()
print(list)

# sort in decending order
list.sort(reverse=True)

print(list)

# matrix list
matrix_list = [
    ['d', 'e', 'f'],
    ['a', 'b', 'c'],
    ['g', 'h', 'i']
]

# sort in accending
matrix_list.sort()
print(matrix_list)

# sort in decending order
matrix_list.sort(reverse= True)
print(matrix_list)




